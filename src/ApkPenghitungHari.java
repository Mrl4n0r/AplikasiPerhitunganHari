    private void btnHitungHariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungHariActionPerformed
        // TODO add your handling code here:
       try {
    // Ambil bulan & tahun langsung dari kalender1
    int tahun = kalender1.getYearChooser().getYear();
    int bulan = kalender1.getMonthChooser().getMonth() + 1; // +1 karena index 0–11

    java.time.LocalDate tanggal = java.time.LocalDate.of(tahun, bulan, 1);
    int jumlahHari = tanggal.lengthOfMonth();

    java.time.LocalDate hariPertama = tanggal.withDayOfMonth(1);
    java.time.LocalDate hariTerakhir = tanggal.withDayOfMonth(jumlahHari);

    txtJumlahHari.setText(String.valueOf(jumlahHari));
    txtHariPertama.setText(hariPertama.getDayOfWeek()
        .getDisplayName(java.time.format.TextStyle.FULL, new java.util.Locale("id", "ID")));
    txtHariTerakhir.setText(hariTerakhir.getDayOfWeek()
        .getDisplayName(java.time.format.TextStyle.FULL, new java.util.Locale("id", "ID")));
} catch (Exception e) {
    javax.swing.JOptionPane.showMessageDialog(this, 
        "Pastikan kalender sudah dipilih dengan benar!", 
        "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
}


    }//GEN-LAST:event_btnHitungHariActionPerformed

    private void btnHitungSelisihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungSelisihActionPerformed
        // TODO add your handling code here:                                                 
                                                    
    try {
        // Ambil tanggal dari kalender utama (kalender1)
        java.util.Calendar cal1 = kalender1.getCalendar();
        java.time.LocalDate t1 = java.time.LocalDate.of(
            cal1.get(java.util.Calendar.YEAR),
            cal1.get(java.util.Calendar.MONTH) + 1, // +1 karena bulan di Calendar mulai dari 0
            cal1.get(java.util.Calendar.DAY_OF_MONTH)
        );

        // Ambil tanggal dari kldSelisih (JDateChooser)
        java.util.Date date2 = kldSelisih.getDate();
        if (date2 == null) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Pilih tanggal pembanding di kalender selisih terlebih dahulu!",
                "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        java.time.LocalDate t2 = date2.toInstant()
            .atZone(java.time.ZoneId.systemDefault())
            .toLocalDate();

        // Hitung selisih hari
        long selisih = java.time.temporal.ChronoUnit.DAYS.between(t1, t2);

        // Tampilkan hasil ke text field
        txtSelisihHari.setText(Math.abs(selisih) + " hari");

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Terjadi kesalahan dalam perhitungan selisih hari.",
            "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    } 

                                                 
    }//GEN-LAST:event_btnHitungSelisihActionPerformed

